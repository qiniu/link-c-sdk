int main()
{
    FLVParam param = 
    {
        .videocodec = FLV_CODECID_H264,
        .audiocodec = FLV_CODECID_AAC,
        .samplerate = FLV_SAMPLERATE_44100HZ,
        .soundtype = FLV_STEREO,
        .samplesize = FLV_SAMPLESIZE_16BIT,
        .write_packet = WriteFlvTag,
    };
    FLVContext *ctx = NULL;
    FILE *fp = fopen( FLV_FILE, "w+" );

    LinkLogInfo("test flv\n");
    param.opaque = fp;
    ctx = FLVNewContext( &param );

    CHECK( ctx );
    CHECK( fp );

    app.running = 1;
    app.fp = fp;
    app.max = MAX_FRAMES;
    app.ctx = ctx;

    FLVWriteHeader( fp );

#ifdef SIM_IPC
    int ret = 0;
    ret = FLVWriteScriptData( ctx, maps, ARRSZ(maps) );
    if ( ret != 0 ) {
        LinkLogError("FLVWriteScriptData error\n");
        goto err;
    }
    FLVFlush( app.ctx );
#else
    FLVWriteMetadata();
#endif

    app.ipc =  NewCoreDevice();
    CHECK( app.ipc );
    app.ipc->init( VideoFrameCallback, AudioFrameCallBack, VIDEO_FILE, AUDIO_FILE  );
    app.ipc->startStream( 0 );

    while ( app.running ) {
        sleep( 1 );
    }

    fclose( fp );
    return 0;
}

