static int VideoFrameCallback ( char *_pFrame, 
                   int _nLen, int _nIskey, double _dTimeStamp, 
                   unsigned long _nFrameIndex, unsigned long _nKeyFrameIndex, 
                   int streamno )
{
    FrameInfo frame;

    if ( app.cnt++ >= app.max ) {
        app.running = 0;
        return 0;
    }
    LinkLogInfo("_nLen = %d\n", _nLen );
    LinkLogInfo("app.cnt = %d\n", app.cnt ); frame.type = TAG_TYPE_VIDEO;
    frame.data = _pFrame;
    frame.len = _nLen;
    frame.timestamp = (int)_dTimeStamp;
    frame.iskey = _nIskey;
    int ret = FLVWriteAVData( app.ctx, &frame );
    if ( ret != 0 ) {
        LinkLogError("FLVAppendAVData error\n");
        exit(1);
    }
    FLVFlush( app.ctx );

    return 0;
}

static int AudioFrameCallBack( char *_pFrame, int _nLen, double _dTimeStamp,
                     unsigned long _nFrameIndex, int streamno )
{
    FrameInfo frame;

    if ( app.cnt++ >= app.max ) {
        app.running = 0;
        return 0;
    }
    frame.type = TAG_TYPE_AUDIO;
    frame.data = _pFrame;
    frame.len = _nLen;
    frame.timestamp = (int)_dTimeStamp;
    frame.iskey = 0;
    int ret = FLVWriteAVData( app.ctx, &frame );
    if ( ret != 0 ) {
        LinkLogError("FLVWriteAVData error\n");
        exit(1);
    }
    FLVFlush( app.ctx );
    return 0;
}


